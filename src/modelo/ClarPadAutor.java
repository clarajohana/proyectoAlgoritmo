package modelo;

public class ClarPadAutor {
	private int autorId;
	private String autorNombre;
	private String autorWeb;
	
	public int getAutorId() {
		return autorId;
	}
	public void setAutorId(int autorId) {
		this.autorId = autorId;
	}
	public String getAutorNombre() {
		return autorNombre;
	}
	public void setAutorNombre(String autorNombre) {
		this.autorNombre = autorNombre;
	}
	public String getAutorWeb() {
		return autorWeb;
	}
	public void setAutorWeb(String autorWeb) {
		this.autorWeb = autorWeb;
	}
}
